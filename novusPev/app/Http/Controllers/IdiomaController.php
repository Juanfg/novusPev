    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $message = "";
        $this->validate($request,[
            "idioma" => "required|string",
            ]);

        $alreadyExists = Idioma::where('idioma',$request->idioma)->count();

        if($alreadyExists == 0){
            //no existe
            Idioma::create(["idioma"=>$request->idioma]);
        }else{
            //existe
            $request->session()->flash('error', "Este idioma ya ha sido registrado");
            return back()->withInput();
        }

        $request->session()->flash("message", "Creado con exito");
        return redirect()->route("idiomas.index");
    }

